<template>
  <div class="relative">

    <div
      v-if="!$store.state.selectedRoom"
      class="alert"
    >
      <div
        class="select-none mt-6 border 
        border-teal-500 shadow rounded-lg py-3"
        style="background-color: rgba(255,255,255,0.6);"
      >
        <p class="px-3">Selecciona una conversaci√≥n para empezar!</p>
      </div>
    </div>

    <div
      ref="messages"
      class="messages-section border rounded-lg border-teal-500 py-2 chat scrollbar overflow-y-auto"
      style="min-height: 20rem; max-height: 25rem;"
    >
      <div v-if="$store.state.selectedRoom">
        <div
          v-for="message in messages"
          :key="message.id"
        >
          <sent-message
            :message="message"
            v-if="message.imOwner"
          />
          <received-message
            :message="message"
            v-else
          />
        </div>
      </div>

    </div>
  </div>

</template>

<script>
import SentMessage from "./SentMessage.vue";
import ReceivedMessage from "./ReceivedMessage.vue";

export default {
  data() {
    return {
      messages: []
    };
  },
  components: {
    SentMessage,
    ReceivedMessage
  },
  methods: {
    scrollToBottom() {
      let sc = this.$refs["messages"];
      sc.scrollTo(0, sc.scrollHeight);
    }
  },
  mounted() {
    this.scrollToBottom();
  },
  computed: {
    selectedRoom() {
      return this.$store.state.selectedRoom;
    }
  },
  watch: {
    selectedRoom(roomId) {
      let messages = this.$store.state.room_messages[roomId];
      this.messages = messages;
      // pedir mensajes antiguos si se da el caso
      if (this.messages.length < 3) {
        console.log("menor que 3");
        // fetchOldMessages
      }
    },
    messages() {
      this.$nextTick(() => {
        this.scrollToBottom();
      });
    }
  }
};
</script>

